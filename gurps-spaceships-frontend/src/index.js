class Spaceship {
    constructor(ship) {
        this.id = ship.id;
        this.name = ship.name;
        this.tech_level = ship.tech_level;
        this.size = ship.size;
        this.steamlined = ship.streamlined;
        this.superscience = ship.superscience;
        this.user_id = ship.user_id;
        this.created_at = ship.created_at;
        this.updated_at = ship.updated_at;
        
    };
}

